# Este é um exemplo simples de algoritmo Ruby que utiliza algumas das palavras reservadas
# e string literals definidos em sua pergunta.

# Declarando variáveis
x = 10
y = 20

# Imprimindo uma string concatenada com o valor da variável x
puts "O valor de x é: #{x}"

# Utilizando um if-else para verificação
if x < y
  puts "x é menor que y"
else
  puts "x é maior que y"
end

# Definindo um método
def greet(name)
  puts "Olá, #{name}!"
end

# Chamando o método com um argumento
greet("João")
